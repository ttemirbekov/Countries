package com.company;

public class Country3 {

   private String name;
   private   String location;
   private String capitall;
   private double area;
   private double population;
   private String currency;
   private String president;
   private String primeminister;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getCapitall() {
        return capitall;
    }

    public void setCapitall(String capitall) {
        this.capitall = capitall;
    }

    public double getArea() {
        return area;
    }

    public void setArea(double area) {
        this.area = area;
    }

    public double getPopulation() {
        return population;
    }

    public void setPopulation(double population) {
        this.population = population;
    }

    public String getCurrency() {
        return currency;
    }

    public void setCurrency(String currency) {
        this.currency = currency;
    }

    public String getPresident() {
        return president;
    }

    public void setPresident(String president) {
        this.president = president;
    }

    public String getPrimeminister() {
        return primeminister;
    }

    public void setPrimeminister(String primeminister) {
        this.primeminister = primeminister;
    }
}
